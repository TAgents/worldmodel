"""Frozen evaluation: CEM-MPC planning on Two-Room.

Do not edit this file across experiments. The autoresearch ratchet relies on
this signal being stable.

Protocol:
- 50 seed-locked (start, goal) pairs (rooms differ; pairs sampled with seed=12345).
- Per evaluation: 3 seeds (1, 2, 3); report mean success over 50*3 = 150 episodes.
- Planner: CEM in latent space. iters=3, candidates=100, horizon=25, top-k=10.
  Execute first 5 actions, then replan (receding-horizon MPC).
- Success: ||agent_pos - goal_pos|| < 0.05 within 100 env steps.

Inputs: a `train.LeWM` model with two methods on a torch device:
    encode(obs_uint8_NHWC[B,64,64,3]) -> z[B,D]
    predict(z[B,D], a[B,2])           -> z_next[B,D]
"""
from __future__ import annotations

import argparse
import importlib

import numpy as np

from envs.two_room import TwoRoom, GOAL_RADIUS, MAX_STEPS

EVAL_PAIRS_SEED = 12345
EVAL_SEEDS = (1, 2, 3)
N_PAIRS = 50
CEM_ITERS = 3
CEM_CANDIDATES = 100
CEM_HORIZON = 25
CEM_TOPK = 10
CEM_EXECUTE = 5


def make_pairs(n: int = N_PAIRS, seed: int = EVAL_PAIRS_SEED) -> list[tuple[np.ndarray, np.ndarray]]:
    """Deterministic (start, goal) pairs in opposite rooms."""
    env = TwoRoom(seed=seed)
    rng = np.random.default_rng(seed)
    env.rng = rng  # so _sample_free_point uses our seed
    pairs = []
    for _ in range(n):
        start = env._sample_free_point()
        goal = env._sample_free_point(other_room_than=start)
        pairs.append((start.astype(np.float32), goal.astype(np.float32)))
    return pairs


def render_goal_obs(goal: np.ndarray, seed: int = 0) -> np.ndarray:
    env = TwoRoom(seed=seed)
    env.reset(start=goal, goal=goal)  # render with agent overlapping goal pixel — only z_g matters
    return env._render()


def cem_plan(model, z0: np.ndarray, zg: np.ndarray, device: str) -> np.ndarray:
    """Returns the next CEM_EXECUTE actions (shape [CEM_EXECUTE, 2])."""
    import torch  # local: keep eval importable without torch for inspection

    mu = np.zeros((CEM_HORIZON, 2), dtype=np.float32)
    sigma = np.ones((CEM_HORIZON, 2), dtype=np.float32)
    z0_t = torch.as_tensor(z0, device=device).unsqueeze(0).expand(CEM_CANDIDATES, -1).contiguous()
    zg_t = torch.as_tensor(zg, device=device).unsqueeze(0)
    for _ in range(CEM_ITERS):
        noise = np.random.randn(CEM_CANDIDATES, CEM_HORIZON, 2).astype(np.float32)
        cands = np.clip(mu[None] + sigma[None] * noise, -1.0, 1.0)
        cands_t = torch.as_tensor(cands, device=device)
        z = z0_t.clone()
        for h in range(CEM_HORIZON):
            z = model.predict(z, cands_t[:, h])
        # score by -||z - zg||
        d = (z - zg_t).pow(2).sum(dim=-1)
        idx = torch.topk(-d, k=CEM_TOPK).indices.cpu().numpy()
        elite = cands[idx]
        mu = elite.mean(axis=0)
        sigma = elite.std(axis=0) + 1e-3
    return mu[:CEM_EXECUTE].copy()


def eval_one(model, pairs, seed: int, device: str) -> float:
    import torch
    np.random.seed(seed)
    successes = 0
    for start, goal in pairs:
        env = TwoRoom(seed=seed)
        obs = env.reset(start=start, goal=goal)
        goal_obs = render_goal_obs(goal, seed=seed)
        with torch.no_grad():
            zg = model.encode(torch.as_tensor(goal_obs, device=device).unsqueeze(0)).squeeze(0).cpu().numpy()
        ok = False
        steps = 0
        while steps < MAX_STEPS:
            with torch.no_grad():
                z0 = model.encode(torch.as_tensor(obs, device=device).unsqueeze(0)).squeeze(0).cpu().numpy()
                plan = cem_plan(model, z0, zg, device=device)
            for a in plan:
                r = env.step(a)
                steps += 1
                obs = r.obs
                if r.success:
                    ok = True
                    break
                if steps >= MAX_STEPS:
                    break
            if ok:
                break
        successes += int(ok)
    return successes / len(pairs)


def evaluate(model, device: str = "cpu") -> dict:
    pairs = make_pairs()
    per_seed = [eval_one(model, pairs, s, device) for s in EVAL_SEEDS]
    return {"mean": float(np.mean(per_seed)), "per_seed": per_seed, "n_pairs": len(pairs)}


def _main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--checkpoint", type=str, default="checkpoints/baseline.pt")
    p.add_argument("--random", action="store_true", help="use a random-init model (sanity: ~0%)")
    p.add_argument("--module", type=str, default="train", help="module exposing LeWM and load_model")
    args = p.parse_args()

    import torch
    mod = importlib.import_module(args.module)
    device = "cuda" if torch.cuda.is_available() else ("mps" if torch.backends.mps.is_available() else "cpu")
    if args.random:
        model = mod.LeWM().to(device).eval()
    else:
        model = mod.load_model(args.checkpoint, device=device).eval()
    res = evaluate(model, device=device)
    print(f"mean={res['mean']:.3f}  per_seed={res['per_seed']}  pairs={res['n_pairs']}")


if __name__ == "__main__":
    _main()
